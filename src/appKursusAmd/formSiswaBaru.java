public void insert_siswa(String id, String nama, String alamat, String jenkel,String jurusan, String email){
        try{
             Class.forName("org.sqlite.JDBC");
           konek = DriverManager.getConnection("jdbc:sqlite:db/dbKursus.db");
           System.out.println("Koneksi Database Sukses");
        PreparedStatement simpan=konek.prepareStatement("insert into siswa values(?,?,?,?,?,?)");
        simpan.setString(1, id);
        simpan.setString(2, nama);
        simpan.setString(3, alamat);
        simpan.setString(4, jenkel);
        simpan.setString(5, jurusan);
        simpan.setString(6, email);
        simpan.execute();
        JOptionPane.showMessageDialog(null, "data berhasil disimpan");
        tampilSiswa();
        bersihkan();
        konek.close();
        }catch(Exception e){
             System.out.println(e);
             JOptionPane.showMessageDialog(null, "data gagal disimpan");
        }
        
    }

