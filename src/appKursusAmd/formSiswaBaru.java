    public void tampilSiswa(){
         try{
        Class.forName("org.sqlite.JDBC");
           konek = DriverManager.getConnection("jdbc:sqlite:db/dbKursus.db");
           System.out.println("Koneksi Database Sukses");
        stat=konek.createStatement();
        rs=stat.executeQuery("select * from siswa");
        DefaultTableModel tb=new DefaultTableModel();
       tb_siswaBaru.setModel(tb);
       tb.addColumn("idsiswa");
       tb.addColumn("nama");
       tb.addColumn("alamat");
       tb.addColumn("jenis kelamin");
       tb.addColumn("jurusan");
       tb.addColumn("email");
       while(rs.next()){
                  Object [] o=new Object[6];
                  o[0]=rs.getString("idsiswa");
                  o[1]=rs.getString("nama");
                  o[2]=rs.getString("alamat");
                  o[3]=rs.getString("jenis_kelamin");
                  o[4]=rs.getString("jurusan");
                  o[5]=rs.getString("email");
                  
                  tb.addRow(o);
                   
               }
       rs.close();
         konek.close();
        }catch(Exception e){
            System.out.println(e);
        }
         
    }

